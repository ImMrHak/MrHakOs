// CGA/VGA 16-color palette as 24-bit RGB values.
static const unsigned int vga_palette[16] = {
    0x000000, 0x0000AA, 0x00AA00, 0x00AAAA,
    0xAA0000, 0xAA00AA, 0xAA5500, 0xAAAAAA,
    0x555555, 0x5555FF, 0x55FF55, 0x55FFFF,
    0xFF5555, 0xFF55FF, 0xFFFF55, 0xFFFFFF,
};

enum VgaColor {
    VGA_COLOR_BLACK = 0,
    VGA_COLOR_BLUE = 1,
    VGA_COLOR_GREEN = 2,
    VGA_COLOR_CYAN = 3,
    VGA_COLOR_RED = 4,
    VGA_COLOR_MAGENTA = 5,
    VGA_COLOR_BROWN = 6,
    VGA_COLOR_LIGHT_GREY = 7,
    VGA_COLOR_DARK_GREY = 8,
    VGA_COLOR_LIGHT_BLUE = 9,
    VGA_COLOR_LIGHT_GREEN = 10,
    VGA_COLOR_LIGHT_CYAN = 11,
    VGA_COLOR_LIGHT_RED = 12,
    VGA_COLOR_LIGHT_MAGENTA = 13,
    VGA_COLOR_LIGHT_BROWN = 14,
    VGA_COLOR_WHITE = 15,
};


// Constructor
Vga::Vga() {
    x = 0;
    y = 0;
    color = VGA_DEFAULT_COLOR;
    cursor_enabled = true;
    clear();
}

// Switch to linear framebuffer mode; clears the screen using the new buffer.
void Vga::init_fb(unsigned int addr, unsigned int pitch,
                  unsigned int width, unsigned int height, unsigned char bpp) {
    fb_base   = (volatile unsigned char*)(unsigned long)addr;
    fb_pitch  = pitch;
    fb_width  = width;
    fb_height = height;
    fb_bpp    = bpp;
    use_fb    = true;
    cursor_enabled = false;   // hardware cursor I/O doesn't apply to framebuffer
    x = 0; y = 0;
    clear();
}

// Write one RGB pixel into the linear framebuffer (BGRA byte order).
void Vga::fb_put_pixel(int px, int py, unsigned int rgb) {
    if ((unsigned int)px >= fb_width || (unsigned int)py >= fb_height) return;
    volatile unsigned char* p = fb_base + (unsigned int)py * fb_pitch
                                + (unsigned int)px * (fb_bpp / 8);
    unsigned char r = (rgb >> 16) & 0xFF;
    unsigned char g = (rgb >>  8) & 0xFF;
    unsigned char b =  rgb        & 0xFF;
    p[0] = b; p[1] = g; p[2] = r;
    if (fb_bpp == 32) p[3] = 0;
}

// Render one character glyph at text-grid position (col, row).
void Vga::fb_draw_char(char c, int col, int row) {
    unsigned int fg_rgb = vga_palette[color & 0x0F];
    unsigned int bg_rgb = vga_palette[(color >> 4) & 0x0F];
    int idx = (unsigned char)c >= 0x20 && (unsigned char)c <= 0x7E
              ? (unsigned char)c - 0x20 : 0;
    for (int gy = 0; gy < 8; gy++) {
        unsigned char bits = fb_font[idx][gy];
        for (int gx = 0; gx < 8; gx++) {
            bool on = (bits >> (7 - gx)) & 1;
            fb_put_pixel(col * 8 + gx, row * 8 + gy, on ? fg_rgb : bg_rgb);
        }
    }
}

// Clear the screen
void Vga::clear() {
    if (use_fb) {
        unsigned int bg_rgb = vga_palette[(color >> 4) & 0x0F];
        unsigned int bpp = fb_bpp / 8;
        for (unsigned int py = 0; py < fb_height; py++) {
            volatile unsigned char* row_ptr = fb_base + py * fb_pitch;
            for (unsigned int px = 0; px < fb_width; px++) {
                volatile unsigned char* p = row_ptr + px * bpp;
                p[0] = bg_rgb & 0xFF;
                p[1] = (bg_rgb >> 8) & 0xFF;
                p[2] = (bg_rgb >> 16) & 0xFF;
                if (fb_bpp == 32) p[3] = 0;
            }
        }
        x = 0; y = 0;
        return;
    }
    for (int i = 0; i < VGA_WIDTH * VGA_HEIGHT; ++i) {
        vga_buffer[i] = (unsigned short)(' ' | (color << 8));
    }
    x = 0;
    y = 0;
}

// Put a character at the current position
void Vga::putChar(char c) {
    if (c == '\n') {
        x = 0;
        y++;
        if (y >= VGA_HEIGHT) scroll();
        if (cursor_enabled) update_cursor();
        return;
    }
    if (c == '\0') return;
    if (x >= VGA_WIDTH) { x = 0; y++; }
    if (y >= VGA_HEIGHT) scroll();

    if (use_fb) {
        fb_draw_char(c, x, y);
    } else {
        vga_buffer[y * VGA_WIDTH + x] = (unsigned short)(c | (color << 8));
    }
    x++;
    if (cursor_enabled) update_cursor();
}

// Put a char at x, y on screen
void Vga::putCharAt(char c, int px, int py) {
    if (c == '\0') c = ' ';
    if (px < 0) px = 0;
    if (py < 0) py = 0;
    if (px >= VGA_WIDTH)  px = VGA_WIDTH  - 1;
    if (py >= VGA_HEIGHT) py = VGA_HEIGHT - 1;

    if (use_fb) {
        fb_draw_char(c, px, py);
    } else {
        vga_buffer[py * VGA_WIDTH + px] = (unsigned short)(c | (color << 8));
    }
    if (cursor_enabled) update_cursor();
}

// Get current X position
int Vga::get_x() {
    return x;
}

// Get current Y position
int Vga::get_y() {
    return y;
}

// Set cursor position
void Vga::set_xy(int new_x, int new_y) {
    // Enforce bounds
    if (new_x >= 0 && new_x < VGA_WIDTH && new_y >= 0 && new_y < VGA_HEIGHT) {
        x = new_x;
        y = new_y;
    }
    if (cursor_enabled) update_cursor();
}

// Scroll the screen up one line
void Vga::scroll() {
    if (use_fb) {
        // Move all pixel rows up by one character height (8 px)
        unsigned int src_off = 8u * fb_pitch;
        unsigned int move_bytes = (unsigned int)(VGA_HEIGHT - 1) * 8u * fb_pitch;
        volatile unsigned char* dst = fb_base;
        volatile unsigned char* src = fb_base + src_off;
        for (unsigned int i = 0; i < move_bytes; i++) dst[i] = src[i];
        // Clear bottom character row
        unsigned int bg_rgb = vga_palette[(color >> 4) & 0x0F];
        unsigned int bpp = fb_bpp / 8;
        volatile unsigned char* last = fb_base + (unsigned int)(VGA_HEIGHT - 1) * 8u * fb_pitch;
        for (unsigned int py = 0; py < 8u; py++) {
            volatile unsigned char* row_ptr = last + py * fb_pitch;
            for (unsigned int px = 0; px < fb_width; px++) {
                volatile unsigned char* p = row_ptr + px * bpp;
                p[0] = bg_rgb & 0xFF;
                p[1] = (bg_rgb >> 8) & 0xFF;
                p[2] = (bg_rgb >> 16) & 0xFF;
                if (fb_bpp == 32) p[3] = 0;
            }
        }
        x = 0; y = VGA_HEIGHT - 1;
        return;
    }
    for (int row = 0; row < VGA_HEIGHT - 1; ++row) {
        for (int col = 0; col < VGA_WIDTH; ++col) {
            vga_buffer[row * VGA_WIDTH + col] = vga_buffer[(row + 1) * VGA_WIDTH + col];
        }
    }
    int last = (VGA_HEIGHT - 1) * VGA_WIDTH;
    for (int col = 0; col < VGA_WIDTH; ++col) {
        vga_buffer[last + col] = (unsigned short)(' ' | (color << 8));
    }
    x = 0;
    y = VGA_HEIGHT - 1;
}

// Set text color
void Vga::set_color(int new_color) {
    color = new_color;
}

// Update cursor follow inputs
void Vga::update_cursor() {
    if (!cursor_enabled || use_fb) return;
    unsigned short pos = y * 80 + x;
    outb(0x3D4, 0x0F);
    outb(0x3D5, (unsigned char)(pos & 0xFF));
    outb(0x3D4, 0x0E);
    outb(0x3D5, (unsigned char)((pos >> 8) & 0xFF));
}

void Vga::set_cursor_enabled(bool enabled) {
    cursor_enabled = enabled;
}

void Vga::force_update_cursor() {
    bool prev = cursor_enabled;
    cursor_enabled = true;
    // Directly update cursor position
    unsigned short pos = y * 80 + x;
    outb(0x3D4, 0x0F);
    outb(0x3D5, (unsigned char)(pos & 0xFF));
    outb(0x3D4, 0x0E);
    outb(0x3D5, (unsigned char)((pos >> 8) & 0xFF));
    cursor_enabled = prev;
}